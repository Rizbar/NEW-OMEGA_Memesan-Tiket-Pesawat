/**
 * Interface untuk menambah dan mengurangi integer
 * @author (NEW-OMEGA)
 * @version (01-12-2021)
 */
public interface TambahKurang
{
    /**
     * Method untuk menambahkan nilai.
     * @param nilai
     */
    void tambah(int nilai);
    
    /**
     * Method untuk mengurangi nilai.
     * @param nilai
     */
    void kurangi(int nilai);
}