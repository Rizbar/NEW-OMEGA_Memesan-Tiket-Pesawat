public class Jadwal {
    private String hari;
    private int jam;
    
    public Jadwal(){
        
    }

    public void setHari(String hari){
        this.hari = hari;
    }

    public String getHari(){
        return hari;
    }

    public void setJam(int jam){
        this.jam = jam;
    }

    public int getJam(){
        return jam;
    }
    
}
